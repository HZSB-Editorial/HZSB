<blockquote>
<p>网络暴力是一种危害严重、影响恶劣的暴力形式，它是指一类由网民发表在网络上的并且具有“诽谤性、诬蔑性、侵犯名誉、损害权益和煽动性”这五个特点的言论、文字、图片、视频。</p>
<p>这一类言论、文字、图片、视频会针对他人的名誉、权益与精神造成损害，人们习惯称其为“网络暴力”。</p>
<p>网络暴力能针对当事人的名誉、权益与精神造成损害，而且它已经打破了道德底线，往往也伴随着侵权行为和违法犯罪行为，亟待运用教育、道德约束、法律等手段进行规范。</p>
<p>网络暴力是网民在网络上的暴力行为，是社会暴力在网络上的延伸。</p>
