<h4 id="扎根边疆-是因为心中有信仰" tabindex="-1"><a class="header-anchor" href="#扎根边疆-是因为心中有信仰" aria-hidden="true">#</a> 扎根边疆，是因为心中有信仰</h4>
<p>2013年9月至2014年9月，郗会锁跨越大半个中国来到新疆巴州，任新疆生产建设兵团农二师华山中学党委委员、副校长。</p>
<p>其间，他创新推行了十八岁成人仪式等一系列改革，深深影响着华山中学并辐射到新疆其他学校，为华山中学及新疆教育发展作出积极贡献，郗会锁也被该校授予“领雁奖”。“会锁有激情、有担当、有作。”华山中学校长邱成国对他赞不绝口。</p>
<p>2016年7月，作为河北省第八批援藏干部中的一员，郗会锁又踏上了奔赴西藏阿里支教的行程。</p>
<p>从平原来到海拔4000多米的高原，阿里地区空气中的含氧量只有内地的一半，头疼、胸闷、失眠等症状让他寝食难安，但当地师生的求知欲望深深地感染了他。有一次到县里讲学，抵达学校已是傍晚，上停电，但老师们打开手机的手电筒，听报告、记笔记、互相交流，直到深夜……那一刻，他眼眶湿润了。</p>
<p>考虑到阿里的老师们赴内地学习成本高，郗会锁想给他们留下一份珍贵的礼物。</p>
<p>于是，他经常深入阿里地区的20多所学校听课、调研、作报告。第一年，他就累计听评课150余节，作报告20余场，座谈交流几十次，写下了近10万字的工作笔记。在他的努力下，阿里教师在自治区017年教学比赛中，捧回4个一等奖等共计16个奖项。他还带领教科所工作人员，初步制定了阿里未来5到10年的“基础教育教学质量提升行动计划”，共计5万多字。2017年10月，他还应邀到拉萨、昌都等区，把先进经验送到当地学校。</p>
<p>“双推进”工作是阿里教育的大事。为了日土县的义务教育均衡验收和普兰县的素质教育评估验收能顺利通过，郗会锁不怕路远道难，几乎走遍了所有学校，仅普兰县就去了八次。经过艰苦努力，日土和兰顺利通过自治区评估验收。郗会锁曾写下了一首《小重山·援藏有感》，表达激动之情：“人至不惑意尤侠。只身向天涯，驰骏马。万里尽享酥油茶。百川源，风雪映莲花。看戈壁黄沙。边地荒崖处，人家。蓝天碧水多豪气。千山祖，丹心展风华。”</p>
<h4 id="提前回程-是因为组织需要" tabindex="-1"><a class="header-anchor" href="#提前回程-是因为组织需要" aria-hidden="true">#</a> 提前回程，是因为组织需要</h4>
<p>2018年5月，支教任务提前结束。在即将返程之际，很多当地老师不畏路途遥远前来送行。一条条洁白的哈达、一杯杯饱含深情的青稞酒、一首首优美动听的歌曲，让郗会锁又一次热泪盈眶。</p>
<p>这一次，他回到衡水中学是因为有了更加重要的担子。</p>
<p>自参加工作以来，他始终坚守一线，20余年送走了14届毕业班，从没有挑拣过一次岗位、没有推脱过一件工作。当了中层干部以后，有人劝他把班主任、任课教师的工作推掉，他却回答：“我不能离开学，和学生打交道是一种乐趣，教书育人是我今生不变的选择。”被任命为学校副校长后，他仍担任班主任、教历史课，送走了一届毕业班。</p>
<p>2013年，郗会锁开始分管德育工作。他积极推行大德育观，实施“三一六”德育工作体系，创新德育活动、学生管理、家校合作等工作，给学生终身难忘的教育。2015年12月，衡中德育团队被河北省委宣部授予“燕赵楷模”称号。</p>
<p>2018年5月，被任命为学校党委书记、校长后，郗会锁仍坚持每周深入课堂听课，并走上讲台，给学生上人文课、思政课。</p>
