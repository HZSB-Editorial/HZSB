<p>小时候，郗会锁就向往成为一名光荣的人民教师。1994年，他迎来人生第一次大考，以优异成绩如愿考入河北师范大学历史教育系。四年后，他背起行囊，坚定而匆忙地走进衡水中学的大门，迈上盼望久的三尺讲台。</p>
<p>“青春是用来奋斗的。”参加工作的第一年，郗会锁就担任了高一班主任。为了备好一节课，他查资料、访老师、研教材、研学生、研教法，提出并实行了历史课堂五五教学法、习题处理三部曲等；为了励每一名学生，他推行了“星光大道”“华山论剑”“值日班长”“班级十最”等一系列班级管理举措，唤醒学生的发展内驱力；为了不断反思提高，他养成了写教育日记的习惯，每天把工作中的一点感悟、一激动、一点挫折写下来，让工作更加理性、更加智慧。各种各样的笔记本，他自己也记不清用了多少个。</p>
<p>郗会锁教历史，学生们都爱听。“诙谐、有趣，上完一节课痛快、充实。”学生们都这样说。</p>
<p>2001年8月，由于工作肯干、业务突出，他被任命为高三年级主任。2004年，郗会锁的孩子出生。他每天晚上查完晚休到医院陪床，早上五点半就到校陪伴学生。正是这一年的教师节，刚刚30岁的郗会锁授予“全国模范教师”荣誉称号，成为衡中建校以来获此殊荣最年轻的教师。</p>
<p>郗会锁陪孩子的时间远没有陪学生的多。他手心里握的是学生双手的温度。他的双手承载着无数学生的未来。</p>
<p>当衡中和他本人一次次被送上热搜，更加为郗校长多年来的付出做了最好的佐证。</p>
